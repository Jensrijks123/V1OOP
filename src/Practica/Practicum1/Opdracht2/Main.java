package Practica.Practicum1.Opdracht2;

public class Main {

    public static void main(String[] arg){

        int count = 1;

        // While loop
        while (count < 11) {
            System.out.println("Getal is: " + count);
            count++;
        }

        int CountDo = 1;

        // Do while loop
        do {
            System.out.println("Do getal is: " + CountDo);
            CountDo++;
        } while (CountDo < 11);

    }


}
